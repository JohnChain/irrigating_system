def Celsius(ADC):
    from math import log, pow
    ADC    = float(ADC)
    ADC_FS = 1023
    R1     = 10000
    A      = 0.00130705
    B      = 0.000214381
    C      = 0.000000093
    Rthr   = R1 * (ADC_FS - ADC) / ADC
    result = float((1 / (A + B * log(Rthr) + C * pow(log(Rthr), 3))) - 273.15)
    return result

